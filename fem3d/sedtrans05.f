C FUNCTION LASTCH
C
C Return the position of the last non space character of a string.
C Return 1 for an empty string
C
      INTEGER FUNCTION LASTCH(CH)
      CHARACTER CH*(*)
      INTEGER I
      DO 10, I=LEN(CH),2,-1
         IF (CH(I:I).NE.' ') GOTO 20
10    CONTINUE
20    LASTCH=I
      END



