C  SUBROUTINE LINETAB
C
C Replace in a string all successive spaces with a tab
C
      SUBROUTINE LINETAB(CH)
      IMPLICIT NONE
      CHARACTER CH*(*),TAB
      INTEGER LENGTH,A,B
      INTEGER LASTCH   ! function
      LENGTH=LASTCH(CH);
      TAB=CHAR(9)
      A=0
      B=0

10    CONTINUE
         A=A+1
         B=B+1
         IF(CH(B:B).EQ.' ') THEN
            IF(B.NE.1) THEN
               CH(A:A)=TAB
            ELSE
               A=0
            ENDIF

20          CONTINUE
            IF(B.LT.LENGTH) THEN
               IF(CH(B+1:B+1).EQ.' ') THEN
                  B=B+1
                  GOTO 20
               ENDIF
            ENDIF

         ELSE
            CH(A:A)=CH(B:B)
         ENDIF
      IF(B.LT.LENGTH) GOTO 10

      IF(A.LT.LENGTH) CH(A+1:LENGTH)=' '
      END      ! end of subroutine LINETAB



